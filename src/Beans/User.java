public class User {

    private String name;
    private String email;
    private String passwordHash;

    public User(String name, String email,  String passwordHash){

    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getPasswordHash() {
        return passwordHash;
    }




}
